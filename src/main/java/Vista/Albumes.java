    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed

        if(jTNumero.getText().length() != 0){
            
            Album a1 = new Album(Integer.parseInt(jTNumero.getText()), jTTitulo.getText(), jTAutor.getText(),Integer.parseInt(jTPrecio.getText()), jTCategoria.getText());
            
            if(con.Insertar(a1)){
                JOptionPane.showMessageDialog(this, "Se ha insertado correctamente");
            }else{
                JOptionPane.showMessageDialog(this, "No se ha insertado correctamente");
            }
        }else{
           JOptionPane.showMessageDialog(this, "Introduce los campos");
        }
        
    }//GEN-LAST:event_jBGuardarActionPerformed

